package com.hzfy.base.di.modules;

import android.content.Context;

import com.hzfy.base.application.AndroidApplication;
import com.hzfy.base.data.app.ApplicationRepository;
import com.hzfy.base.data.app.IApplicationRepository;

import javax.inject.Singleton;

import dagger.Module;
import dagger.Provides;

/**
 * Dagger module that provides objects which will live during the application lifecycle.
 */
@Module
public class ApplicationModule {
    private final AndroidApplication application;

    public ApplicationModule(AndroidApplication application) {
        this.application = application;
    }

    @Provides
    @Singleton
    Context provideApplicationContext() {
        return this.application;
    }

    @Provides
    @Singleton
    IApplicationRepository provideApplicationRepository(ApplicationRepository applicationRepository) {
        return applicationRepository;
    }

}
